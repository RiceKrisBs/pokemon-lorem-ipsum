/**
 * Structured Pokémon locations for the text generator
 */

// Define types for location information
export interface LocationBase {
  name: string;
}

export interface Settlement extends LocationBase {
  type: 'city' | 'town' | 'island' | 'settlement';
}

export interface Landmark extends LocationBase {
  type: 'landmark';
}

export interface Route extends LocationBase {
  type: 'numbered route' | 'named route';
  connects: string[]; // Names of the locations this route connects
}

export interface Gym extends LocationBase {
  type: 'gym';
  order: number;
  badge: string;
  pokemonType: string;
  leader: string;
}

export interface RegionData {
  name: string;
  settlements: Settlement[];
  landmarks: Landmark[];
  routes: Route[];
  gyms?: Gym[];
}

